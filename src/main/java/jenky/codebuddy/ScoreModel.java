package jenky.codebuddy;

public class ScoreModel {

    private long id;
    private long score;

    public ScoreModel(long score) {
        this.id = id;
        this.score = score;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getScore() {
        return score;
    }

    public void setScore(long score) {
        this.score = score;
    }


}